// ════════════════════════════════════════════════════════════════════════
// Per-language synonym groups. Unlike SYNONYMS above (English headword →
// English near-synonyms, with Ukrainian nuance notes), these are genuine
// near-synonym groups within each target language itself — not translations
// of the English groups — so they only make sense displayed in that
// language, with no cross-language member lookup/click-through.
// ════════════════════════════════════════════════════════════════════════

export function buildSynonymReverse(dict: Record<string, SynonymEntry[]>): Map<string, string> {
  const rev = new Map<string, string>();
  for (const [head, members] of Object.entries(dict)) {
    for (const m of members) rev.set(m.word, head);
  }
  return rev;
}


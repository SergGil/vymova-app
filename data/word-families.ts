// Reverse index: member word → base word (built once at module load)
export const WORD_FAMILY_REVERSE: Map<string, string> = new Map();
for (const [base, members] of Object.entries(WORD_FAMILIES)) {
  for (const m of members) WORD_FAMILY_REVERSE.set(m, base);
}

// ════════════════════════════════════════════════════════════════════════
// Per-language word families. Like WORD_FAMILIES above (English base word
// → English derived forms), these are genuine derivational families
// within each target language itself — not translations of the English
// groups. For Hebrew/Arabic (root-based morphology) and Chinese/Japanese
// (shared-character morphemes) the "family" is built around that
// language's own organizing principle rather than Latin-style suffixes.
// ════════════════════════════════════════════════════════════════════════

export function buildFamilyReverse(dict: Record<string, string[]>): Map<string, string> {
  const rev = new Map<string, string>();
  for (const [base, members] of Object.entries(dict)) {
    for (const m of members) rev.set(m, base);
  }
  return rev;
}

